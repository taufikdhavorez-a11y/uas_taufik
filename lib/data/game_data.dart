  static List<LevelData> getLevelsByDifficulty(Difficulty difficulty) {
    return allLevels.where((l) => l.difficulty == difficulty).toList();
  }

  static LevelData? getNextLevel(LevelData current) {
    final levelsInDifficulty = getLevelsByDifficulty(current.difficulty);
    final index = levelsInDifficulty.indexWhere((l) => l.levelNumber == current.levelNumber);
    
    if (index != -1 && index < levelsInDifficulty.length - 1) {
      return levelsInDifficulty[index + 1];
    }
    return null;
  }
